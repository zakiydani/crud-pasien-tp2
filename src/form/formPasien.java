    private void button_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_editActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn=(java.sql.Connection) connection.database.connectDB();
            String sql = "UPDATE data_pasien SET nama_pasien=?, alamat=?, tanggal_lahir=?, WHERE NIK=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            System.out.println(sql);
   
            pst.setString(1, nama_pasien.getText());
            pst.setString(2, alamat.getText());
            pst.setString(3, tanggal_lahir_pasien);
            pst.setInt(4, Integer.parseInt(NIK.getText()));
            // Menjalankan perintah update
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil di edit");
            showData();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Proses edit gagal");
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_button_editActionPerformed

    private void button_pilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_pilihActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn=(java.sql.Connection) connection.database.connectDB();
            String sql = "Select * from data_pasien where NIK='"+search_value.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet response = pst.executeQuery(sql);
            if (response.next()){
                nama_pasien.setText(response.getString(1));
                NIK.setText(response.getString(2));
                alamat.setText(response.getString(3));
                tanggal_lahir.setDate(response.getDate(4));
                JOptionPane.showMessageDialog(null,"Data Pasien : "
                + search_value.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
            }
        } catch (SQLException e){
            
        }
        search_value.requestFocus();     

    }//GEN-LAST:event_button_pilihActionPerformed


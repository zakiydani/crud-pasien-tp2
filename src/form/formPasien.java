    public void showData(){
        DefaultTableModel tabel=new DefaultTableModel();
        tabel.addColumn("nama_pasien");
        tabel.addColumn("NIK");
        tabel.addColumn("alamat");
        tabel.addColumn("tanggal_lahir");
        try {
            java.sql.Connection conn = (java.sql.Connection) connection.database.connectDB();
            String sql = "select * from data_pasien";
            java.sql.PreparedStatement pst= conn.prepareStatement(sql);
            ResultSet response = pst.executeQuery(sql);
            while(response.next())
                {
                    tabel.addRow(new Object[]{
                        response.getString(1),
                        response.getString(2),
                        response.getString(3),
                        format.format(response.getDate(4)),
                    });
                   
                }
            jTable1.setModel(tabel);
        } catch (SQLException e){
            
        }
    }

    
